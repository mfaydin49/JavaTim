package section_4;

public class A_Keyword_Expression_Statements {
    public static void main(String[] args) {
        double kilometres = (100 * 1.609344); //kilometres = (100*1.609344) is an expression
        //expression includes variable, values and operator
        System.out.println("this is an expression");

        int myVariable = 50; // this entire line is Statement
        myVariable++;
        myVariable--;// it is also statement. we need semi-colon.
        System.out.println("this whole line is statement");
    }
}
